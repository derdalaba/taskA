package exam.kit.game;

/**
 * Represents the effectiveness of elements against each other.
 * @author uepiy
 */
public enum Effectiveness {
    /**
     * Represents the effectiveness of an element against another element as weak.
     */
    WEAK,
    /**
     * Represents the effectiveness of an element against another element as normal.
     */
    NORMAL,
    /**
     * Represents the effectiveness of an element against another element as strong.
     */
    STRONG
}
