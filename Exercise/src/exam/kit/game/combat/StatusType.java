package exam.kit.game.combat;

/**
 * Represents the different types of stats a monster has.
 * @author uepiy
 */

public enum StatusType {
    /**
     * Represents the attack stat of a monster.
     */
    ATK,
    /**
     * Represents the defense stat of a monster.
     */
    DEF,
    /**
     * Represents the speed stat of a monster.
     */
    SPD,
    /**
     * Represents the precision stat of a monster.
     */
    PRC,
    /**
     * Represents the agility stat of a monster.
     */
    AGL;
}
